                                <p><strong>Politica de utilizare cookie-uri și scopurile sale</strong></p>


                                <p>Această politică se referă la cookie-urile și la tehnologiile similare folosite, după
                                    caz, în website-ul&nbsp;<strong>www.aromaflames.shop</strong></p>


                                <p>Cookie-urile sunt folosite pentru următoarele scopuri:</p>


                                <ul>
                                    <li>de funcționare a site-ului,</li>


                                    <li>de analiză a comportamentului vizitatorilor site-ului</li>


                                    <li>pentru publicitate</li>
                                </ul>


                                <p><strong>2.&nbsp;Ce sunt cookie-urile?</strong></p>


                                <p>Cookie-urile sunt fișiere de mici dimensiuni ce stochează informații despre tine,
                                    browser-ul tău web și comportamentul tău pe internet. Acestea sunt păstrate pe
                                    computerul sau dispozitivul tău și pot fi folosite de saiturile sau de aplicațiile
                                    web pentru a ajusta experiența ta online.</p>


                                <p><strong>3. La ce sunt utilizate cookie-urile?</strong></p>


                                <p>Cookie-urile sunt transmise între un expeditor (de obicei un sait web sau o aplicație
                                    web) și un destinatar (dispozitivul tău). Un cookie este creat și interpretat de
                                    către expeditor, în timp ce destinatarul doar îl păstrează și îl trimite înapoi dacă
                                    expeditorul cere asta.</p>


                                <p>Atunci când navighezi pe internet, expeditorul este serverul care găzduiește un sait
                                    web și destinatarul este browser-ul web care vizitează acel sait. Scopul lor este de
                                    a identifica utilizatorul, de a verifica activitatea lui din trecut pe acel sait și
                                    de a oferi conținut specific bazându-se pe aceste date.</p>


                                <p>Acestea mai sunt utilizate în pregătirea unor statistici anonime agregate care ne
                                    ajută să înțelegem cum un utilizator beneficiază de paginile noastre de internet,
                                    permițându-ne îmbunătățirea structurii și conținutului lor, fără a permite
                                    identificarea personală a utilizatorului.</p>


                                <p><strong>4.&nbsp;Cum sunt folosite cookie-urile de către acest site?</strong></p>


                                <p>Cookie-urile strict necesare sunt esențiale pentru buna funcționare a site-ului,
                                    fiind setate pe dispozitivul dumneavoastră la accesarea site-ului sau în urma
                                    acțiunilor efectuate în site.</p>


                                <p><strong>5. Ce tipuri de cookie folosim?</strong></p>


                                <p>Folosim trei tipuri de cookie-uri:</p>


                                <p><strong>Cookie-uri de sesiune</strong></p>


                                <p>Există într-o memorie temporară până când navigatorul de internet este închis.</p>


                                <p><strong>Cookie-uri persistente</strong></p>


                                <p>Sunt fișiere temporare ce rămân în terminalul utilizatorului până la terminarea
                                    sesiunii sau închiderea aplicației. Cookie-urile fixe rămân pe terminalul
                                    utilizatorului pe o perioadă determinată de parametrii cookie-ului sau până sunt
                                    șterse manual de utilizator.</p>


                                <p><strong>Cookie-uri sigure</strong></p>


                                <p>Un cookie criptat funcționează doar printr-o conexiune securizată HTTPS. Aceste
                                    cookie-uri&nbsp; conțin informații despre utilizator și sunt utilizate mai ales de
                                    saituri pe care utilizatorii efectuează tranzacții financiare.</p>


                                <p><strong>6. Cum sunt folosite cookie-urile de către acest site?</strong></p>


                                <p>O vizită pe acest site poate plasa următoarele tipuri de cookie-uri:</p>


                                <ul>
                                    <li>cookie-uri strict necesare pentru funcționarea site-ului</li>


                                    <li>cookie-uri de analiză</li>
                                </ul>


                                <p>Cookie-urile sunt sunt esențiale pentru buna funcționare a site-ului, fiind setate pe
                                    dispozitivul dumneavoastră la accesarea site-ului sau în urma acțiunilor
                                    efectuate.</p>


                                <p><strong>7. Cookie-urile prelucrează date personale?</strong></p>


                                <p>Cookie-urile pe care le utilizăm nu solicită informații cu caracter personal pentru a
                                    putea fi utilizate și în același nici nu identifică personal utilizatorii de
                                    internet.</p>


                                <p><strong>8. Cum se blocheaza sau se șterg cookie-urile?</strong></p>


                                <p>Dacă decideți că nu sunteți mulțumit cu utilizarea de cookie-uri pe acest site, le
                                    puteți șterge cu ușurință din folderul cookie al browser-ului. Asemenea, puteți seta
                                    browser-ul pentru a bloca cookie-uri sau pentru a trimite o notificare de avertizare
                                    înainte ca un cookie sa fie stocat pe calculatorul dumneavoastră. Puteti găsi mai
                                    multe informații despre cookies din meniul browser-ului. Când blocați cookie-uri,
                                    puteți utiliza în continuare site-urile noastre, dar acest lucru va avea un impact
                                    negativ asupra gradului de utilizare a acestor site-uri web.</p>


